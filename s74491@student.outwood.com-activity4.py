def main():
    grades = float(input("grades input "))
    if grades > 90:
        print("grade 9")
    elif grades > 80:
        print("grade 8")
    elif grades > 70:
        print("grade 7")
    elif grades > 60:
        print("grade 6")
    elif grades > 50:
        print("grade 5")
    else:
        print("fail")
main()